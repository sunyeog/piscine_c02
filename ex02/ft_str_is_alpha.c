int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			return (1);
		}
		else if (str[i] >= 'A' && str[i] <= 'Z')
		{
			return (1);
		}
		else if (str[i] == '\0')
		{
			return (1);
		}
		else
		{
			return (0);
		}
		i++;
	}
}
